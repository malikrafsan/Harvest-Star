getFairy :-
    (
        (random(0, 9, Rand), Rand == 8), !, (
            sleep(1),
            showFairy,
            write('Behind your deep sleep, '),
            write('there\'s a fairy that wants to talk to you.'), nl,
            write('She said she will help you to move into any position you want '),
            write('without draining your stamina.'), nl,
            write('Do you want to take this chance? (yes/[no]) '),
            (read(MoveFairy), MoveFairy == 'yes'), !, (
                getPositionUntilValid
            ) ; (
                write('Until next time!'), nl
            )
        ) ; true
  ).

getPositionUntilValid :-
    write('Type abscissa (X) to move on: '), read(X),
    write('Type ordinate (Y) to move on: '), read(Y),
    isInMap(X,Y), !, (
        map_object('o', X, Y), !, ( 
            write('You can\'t move into water! Try again.'), nl,
            getPositionUntilValid
        ) ; (
            retractall(player_position(_, _)),
            asserta(player_position(X, Y))
        )
    ) ; (
        write('You can\'t move outside the map! Try again.'), nl,
        getPositionUntilValid
    ).
